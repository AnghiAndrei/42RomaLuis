#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int		i;
	int		i2;
	int		a;

	i = 0;
	i2 = size - 1;
	size /= 2;
	while (i != size)
	{
		a = tab[i];
		tab[i] = tab[i2];
		tab[i2] = a;
		i2--;
		i++;
	}
}
