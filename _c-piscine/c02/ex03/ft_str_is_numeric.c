#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	i;
	int	ris;

	i = 0;
	ris = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '0' || str[i] == '1' || str[i] == '2' || str[i] == '3'
			|| str[i] == '4' || str[i] == '5' || str[i] == '6' || str[i] == '7'
			|| str[i] == '8' || str[i] == '9')
			ris++;
		i++;
	}
	if (ris == i)
		return (1);
	else
		return (0);
}
