#include <unistd.h>

void	ft_putnbr(int nb)
{
	char		t;

	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb = -nb;
		}
		if (nb >= 10)
			ft_putnbr(nb / 10);
		t = nb % 10 + '0';
		write(1, &t, 1);
	}
}
