#include <unistd.h>

int	ft_atoi(char *str)
{
	int	result;
	int	sign;

	sign = 5;
	result = 0;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + (*str - '0');
		str++;
	}
	if (sign < 5)
		result = result * -1;
	return (result);
}
