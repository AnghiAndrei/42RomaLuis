#include "../ft_printf.h"

int	ft_putnbr(int n)
{
	char		c;
	int			n2;

	n2 = n;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n = -n;
		}
		if (n >= 10)
			ft_putnbr(n / 10);
		c = n % 10 + '0';
		write(1, &c, 1);
	}
	return (len_int(n2));
}
