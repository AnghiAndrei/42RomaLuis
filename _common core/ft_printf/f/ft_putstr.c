#include "../ft_printf.h"

int	ft_putstr(char *s)
{
	int	c;

	c = 0;
	if (s != NULL)
	{
		while (*s)
		{
			c = c + ft_putchar(*s);
			s++;
		}
	}
	else
	{
		write(1, "(null)", 6);
		return (6);
	}
	return (c);
}
