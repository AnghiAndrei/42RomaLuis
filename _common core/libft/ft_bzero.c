#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	unsigned char	*p1;
	size_t			i;

	p1 = (unsigned char *)s;
	i = 0;
	while (i != n)
	{
		p1[i] = 0;
		i++;
	}
	return (s);
}
