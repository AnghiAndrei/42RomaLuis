#include "philosophers.h"

int	ft_atoi(const char *string)
{
	int		i;
	int		res;

	i = 0;
	res = 0;
	while (string[i] >= '0' && string[i] <= '9')
	{
		res = (res * 10) + (string[i] - '0');
		i++;
	}
	return (res);
}
